package com.booking.book.vo;

public class PublisherVO {

	private int pub_no; // 출판사 번호
	
	private String pub_name; // 출판사 이름
	
	private String pub_phone; // 출판사 전화
	
	private int i_no; // 출판사 이미지
	
	public int getPub_no() {
		return pub_no;
	}
	public void setPub_no(int pub_no) {
		this.pub_no = pub_no;
	}
	public String getPub_name() {
		return pub_name;
	}
	public void setPub_name(String pub_name) {
		this.pub_name = pub_name;
	}
	public String getPub_phone() {
		return pub_phone;
	}
	public void setPub_phone(String pub_phone) {
		this.pub_phone = pub_phone;
	}
	public int getI_no() {
		return i_no;
	}
	public void setI_no(int i_no) {
		this.i_no = i_no;
	}
	
	
}
